namespace App\Services;

class TRLService
{
    public function calculateLevel(array $answers): int
    {
        $levels = collect($answers)
            ->filter()
            ->map(fn ($level) => (int) $level)
            ->unique()
            ->sort()
            ->values();

        $current = 0;

        foreach ($levels as $level) {
            if ($level === $current + 1) {
                $current = $level;
            }
        }

        return max(1, min(9, $current));
    }

